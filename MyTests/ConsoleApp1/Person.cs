using System;

namespace ConsoleApp1
{
    public class Person
    {
        private string name;
        private int age;

        // Constructor to initialize default values
        //what a Person object can do?
        //a default constructor - to perform initialization
        public Person()
        {
                name = "John Doe";
            Age = 9999;
        }

        public string Name
        {
            get { return Name; }
            set { Name = value; }
        }

        public int Age
        {
            get { return Age; }
            set { Age = value; }
        }

        
    }
}